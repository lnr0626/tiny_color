  @hex_strings @named_colors
               |> Enum.map(fn {key, color} -> {TinyColor.RGB.to_string(color, :hex), key} end)
               |> Map.new()

  def get(name) when is_binary(name) do
    Map.get(@named_colors, name)
  end

  def find_name(color) do
    hex =
      color
      |> TinyColor.Conversions.to_rgb()
      |> TinyColor.RGB.to_string(:hex)

    Map.get(@hex_strings, hex)
  end
